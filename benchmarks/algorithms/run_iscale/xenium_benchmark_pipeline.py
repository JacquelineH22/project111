def main():
    parser = argparse.ArgumentParser(description="Xenium Benchmarking Pipeline for iSCALE")
    parser.add_argument('--h5ad',         required=True,        help='Path to simulated_data.h5ad')
    parser.add_argument('--he_image',     required=True,        help='Path to H&E image')
    parser.add_argument('--output',       required=True,        help='Output directory')
    parser.add_argument('--capture_size', type=float, default=3.2,    help='Capture size in mm (default 3.2)')
    parser.add_argument('--pixel_scale',  type=float, default=0.2125, help='µm/pixel (default 0.2125)')
    parser.add_argument('--window_size',  type=float, default=55,     help='Spot diameter in µm (default 55)')
    parser.add_argument('--min_spots',    type=int,   default=200,    help='Min spots per capture (default 200)')
    args = parser.parse_args()

    XeniumBenchmarkPipeline(
        h5ad_path=args.h5ad,
        he_path=args.he_image,
        output_base=args.output,
        capture_size_mm=args.capture_size,
        pixel_scale=args.pixel_scale,
        window_size=args.window_size,
        min_spots=args.min_spots,
    ).run()


if __name__ == '__main__':
    main()